#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	dex;

	if (!s)
		return ;
	dex = 0;
	while (s[dex] != '\0')
	{
		write(fd, &s[dex], 1);
		dex++;
	}
}
